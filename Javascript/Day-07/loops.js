// Normal Loop

for (let i = 1; i <= 10; i++){
    console.log(i); // 1 to 10
}


// List Item
let fruit_list = ['apple', 'banana', 'orange', 'Kiwi'];
console.log(fruit_list[0]);

for (let i = 0; i < fruit_list.length; i++){
    console.log(fruit_list[i]); // show index wise
}


// reverse loop
for (let x = 10; x >= 1; x--){
    console.log(x)
}
